package be.appify.prefab.core.annotations.rest;

/** HTTP methods used in REST endpoints. */
public interface HttpMethod {

    /** HTTP PUT method. */
    String PUT = "PUT";
    /** HTTP POST method. */
    String POST = "POST";
    /** HTTP GET method. */
    String GET = "GET";
    /** HTTP DELETE method. */
    String DELETE = "DELETE";
    /** HTTP PATCH method. */
    String PATCH = "PATCH";
    /** HTTP OPTIONS method. */
    String OPTIONS = "OPTIONS";
    /** HTTP HEAD method. */
    String HEAD = "HEAD";

}

